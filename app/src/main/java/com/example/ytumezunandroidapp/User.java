package com.example.ytumezunandroidapp;

public class User {

    public String name, surname, email, password, birthDate, graduateDate, photo;

    public User(String name, String surname, String email, String password, String birthDate, String graduateDate, String photo) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.password = password;
        this.birthDate = birthDate;
        this.graduateDate = graduateDate;
        this.photo = photo;
    }
}